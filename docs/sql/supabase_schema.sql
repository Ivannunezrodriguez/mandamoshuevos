-- 1. Tabla de Perfiles (Extiende la info de usuario)
-- Se vincula automáticamente cuando un usuario se registra
create table public.profiles (
  id uuid references auth.users not null primary key,
  username text unique,
  full_name text,
  address text,
  dni text,
  phone text,
  role text default 'user',
  updated_at timestamp with time zone
);

-- 2. Tabla de Pedidos
create table public.orders (
  id uuid default gen_random_uuid() primary key,
  user_id_ref text, -- Por ahora guardamos el username/id de referencia
  total numeric,
  status text default 'pending', -- pending, paid, shipped, delivered
  delivery_date date,
  payment_method text,
  invoice_number text,
  created_at timestamp with time zone default timezone('utc'::text, now())
);

-- 3. Tabla de Items del Pedido
create table public.order_items (
  id bigint generated by default as identity primary key,
  order_id uuid references public.orders(id) on delete cascade,
  product_id text,
  quantity integer,
  price_at_purchase numeric
);

-- 4. Tabla de Inventario
create table public.inventory (
  product_id text primary key,
  stock_quantity integer default 0,
  min_stock_alert integer default 10,
  updated_at timestamp with time zone default timezone('utc'::text, now())
);

