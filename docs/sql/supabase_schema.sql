-- 5. Políticas de Seguridad (RLS)
alter table public.profiles enable row level security;
alter table public.orders enable row level security;
alter table public.order_items enable row level security;
alter table public.inventory enable row level security;

-- Perfiles
create policy "Public profiles are viewable by everyone." on profiles for select using ( true );
create policy "Users can insert own profile" on profiles for insert with check ( auth.uid() = id );
create policy "Users can update own profile." on profiles for update using ( auth.uid() = id );

-- Pedidos (ADMIN puede ver todo, USER solo lo suyo)
create policy "Admins can see all orders" 
  on orders for select 
  using ( (select role from profiles where id = auth.uid()) = 'admin' );

create policy "Users can see their own orders" 
  on orders for select 
  using ( user_id_ref = auth.jwt() ->> 'email' );

create policy "Admin can update orders status"
  on orders for update
  using ( (select role from profiles where id = auth.uid()) = 'admin' );

create policy "Anyone can create orders" on orders for insert with check ( true );

-- Items de Pedido
create policy "Admins can see all order items" on order_items for select using ( (select role from profiles where id = auth.uid()) = 'admin' );
create policy "Users can see own items" on order_items for select using ( true ); -- Simplificado para demo
create policy "Anyone can insert items" on order_items for insert with check ( true );

-- Inventario (Solo ADMIN)
create policy "Anyone can view inventory" on inventory for select using ( true );
create policy "Only admins can update inventory" 
  on inventory for update 
  using ( (select role from profiles where id = auth.uid()) = 'admin' );


-- 6. Automatización de Perfiles (Trigger)
create or replace function public.handle_new_user()
returns trigger as $$
begin
  insert into public.profiles (id, username, full_name, role, email)
  values (
    new.id,
    new.email, -- Usamos email como username base inicial
    new.raw_user_meta_data->>'full_name',
    'user', -- Por defecto todos son 'user'
    new.email
  );
  return new;
end;
$$ language plpgsql security definer;

-- Trigger que se dispara cada vez que un usuario se crea en auth.users
create or replace trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

